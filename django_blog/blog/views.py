from django.contrib.auth.decorators import login_required

@login_required
def profile(request):
    if request.method == 'POST':
        user = request.user
        user.email = request.POST['email']
        user.save()
        return redirect('profile')
    return render(request, 'registration/profile.html', {'user': request.user})

